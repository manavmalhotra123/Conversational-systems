a = 56
b = 10


result_and = a & b
print("a & b:", result_and)

result_or = a | b
print("a | b:", result_or)

result_xor = a ^ b
print("a ^ b:", result_xor)

a_left_shift = a << 2
b_left_shift = b << 2
print("a left shifted by 2 bits:", a_left_shift)
print("b left shifted by 2 bits:", b_left_shift)

a_right_shift = a >> 2
b_right_shift = b >> 4
print("a right shifted by 2 bits:", a_right_shift)
print("b right shifted by 4 bits:", b_right_shift)
